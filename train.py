def remove_unknown_class_from_labels(labels_path):
    """
    Menghapus semua baris yang berisi class unknown (class_id = 2)
    dari file label .txt
    """
    if not os.path.exists(labels_path):
        print(f"⚠️ Folder tidak ditemukan: {labels_path}")
        return 0, 0

    label_files = [f for f in os.listdir(labels_path) if f.endswith('.txt')]
    total_removed = 0
    total_files_modified = 0

    for label_file in label_files:
        file_path = os.path.join(labels_path, label_file)

        # Baca semua baris
        with open(file_path, 'r') as f:
            lines = f.readlines()

        # Filter: hanya simpan baris dengan class 0 (fire) atau class 1 (smoke)
        filtered_lines = []
        removed_count = 0

        for line in lines:
            parts = line.strip().split()
            if len(parts) >= 1:
                class_id = int(parts[0])
                if class_id == 0 or class_id == 1:
                    filtered_lines.append(line)
                elif class_id == 2:
                    removed_count += 1

        # Jika ada yang dihapus, tulis ulang file
        if removed_count > 0:
            total_removed += removed_count
            total_files_modified += 1
            with open(file_path, 'w') as f:
                f.writelines(filtered_lines)

            # Jika file menjadi kosong, hapus file label (tidak perlu file kosong)
            if len(filtered_lines) == 0:
                os.remove(file_path)
                print(f"   🗑️ {label_file}: semua label dihapus (file dihapus)")
            else:
                print(f"   ✂️ {label_file}: dihapus {removed_count} unknown")

    return total_removed, total_files_modified

