def verify_no_unknown(labels_path, path_name):
    """Memastikan tidak ada lagi class unknown di file label"""
    if not os.path.exists(labels_path):
        print(f"⚠️ {path_name}: folder tidak ditemukan")
        return 0

    unknown_found = 0
    for label_file in os.listdir(labels_path):
        if label_file.endswith('.txt'):
            file_path = os.path.join(labels_path, label_file)
            with open(file_path, 'r') as f:
                for line in f:
                    parts = line.strip().split()
                    if len(parts) >= 1 and int(parts[0]) == 2:
                        unknown_found += 1
                        print(f"   ⚠️ Masih ada unknown di {label_file}")

    if unknown_found == 0:
        print(f"   ✅ {path_name}: tidak ada class unknown")
    else:
        print(f"   ❌ {path_name}: masih ada {unknown_found} unknown!")

    return unknown_found

